# Variables in Python

a = 5 # Integar Variable
print(a)

b = 3.22 # Decimal Variable
print(b)

c = "zakir" # String Variable
print(c)

Zakir = 433 # Assigning Variable to Variable
d = Zakir
print(d)

x = 23
y = 33
print(x+2) # Adding Number/Variable to Variable
print(x+y)

p,q,r = 2,5,7 # Multiple Variable
print(p,q,r)

